import { put } from "@vercel/blob";

export async function generateFortuneImage(prompt: string) {
  // 1) generate image ‡∏à‡∏≤‡∏Å AI (‡∏ü‡∏£‡∏µ)
  const aiImageUrl =
    "https://image.pollinations.ai/prompt/" +
    encodeURIComponent(prompt + ", thai fortune style, mystic");

  // 2) download image
  const res = await fetch(aiImageUrl);
  if (!res.ok) throw new Error("AI image failed");

  const buffer = Buffer.from(await res.arrayBuffer());

  // 3) upload to Vercel Blob
  const fileName = `fortune-${Date.now()}.png`;

  const blob = await put(fileName, buffer, {
    access: "public",
    contentType: "image/png",
  });

  return blob.url;
}

